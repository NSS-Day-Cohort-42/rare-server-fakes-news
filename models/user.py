class User():
    def __init__(self, id, avatar, first_name, last_name, display_name, password, email, creation, active):
        self.id = id
        self.avatar = avatar
        self.first_name = first_name
        self.last_name = last_name
        self.display_name = display_name
        self.password = password
        self.email = email
        self.creation = creation
        self.active = active